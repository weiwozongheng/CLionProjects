#include <stdio.h>

// 说明
// 1.函数名字 test
// 2.函数没有返回， void
// 3.完成功能 :传入一个数+1
void test(int n) {
    int n2 = n + 1;
    printf("\nn2=%d", n2);
}

double getSum(int n1, int n2) {
    // int -> double(自动转换)
    return n1 + n2;
}

void main() {
    int num = 6;

    // double -> int
    double res = getSum(1, 9);

    test(num);

    printf("\nres=%.2f", res); // 10

    printf("\nokook~");

    getchar();
}
